package com.example;

public class TestService {

    /**
 * Retorna el doble del valor recibido.
 *
 * @param a valor de entrada
 * @return el doble del valor ingresado
 */
public int doubleValue(int a) {
    return a * 2;
}

/**
 * Suma dos números.
 *
 * @param a valor de entrada
 * @return resultado de la suma
 */
public int sum(int a) {
    return a + 2;
}
}



